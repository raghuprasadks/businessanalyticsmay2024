"""
comparision - >,<,<=,>=,==,!=
"""
num1=100
num2=200
num3=100
print("Comparision Operator  > ")
res = num1>num2
print(num1," > ",num2," = ",res)
print("Comparision Operator  == ")
res = num1==num3
print(num1," == ",num3," = ",res)
print("Comparision Operator  != ")
res = num1!=num3
print(num1," != ",num3," = ",res)

