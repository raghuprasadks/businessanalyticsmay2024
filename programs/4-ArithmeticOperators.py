"""
Arithmetic operations - +,-,*,/,//,**,%
"""
print("Arithmetic Operator +")
num1 = 100
num2 = 200
print(num1+num2)
res = num1+num2
print(num1 , " + ",num2," = ",res)
print("Arithmetic Operator -")
res = num1-num2
print(num1 , " - ",num2," = ",res)
print("Arithmetic Operator *")
res = num1*num2
print(num1 , " * ",num2," = ",res)
num3=9
num4=2
print("Arithmetic Operator /")
res = num3/num4
print(num3 , " / ",num4," = ",res)
print("Arithmetic Operator //")
res = num3//num4
print(num3 , " // ",num4," = ",res)
print("Arithmetic Operator % -remainder")
res = num3%num4
print(num3 , " % ",num4," = ",res)
print("Arithmetic Operator ** -exponent")
num5=2
num6=3
res = num5**num6
print(num5 , " ** ",num6," = ",res)
